// ========== ПРОГРЕСС ==========
function renderProgress() {
  const total = 30;
  let done = 0;
  for (let i = 1; i <= total; i++) {
    if (completedDays[i]) done++;
  }
  const percent = (done / total) * 100;
  document.getElementById('progressBar').style.width = `${percent}%`;
}

// ========== ОТОБРАЖЕНИЕ ДНЯ ==========
function renderDay(dayNum) {
  const w = workouts.find(item => item.day === dayNum);
  const m = meals.find(item => item.day === dayNum);

  if (!w || !m) {
    document.getElementById('workouts').innerHTML = `<div class="day-card"><h2>Ошибка: данные не найдены</h2></div>`;
    return;
  }

  // Обновляем заголовок дня
  document.getElementById('dayCounter').textContent = `День ${dayNum}`;

  // Обновляем кнопку "Выполнено"
  const markBtn = document.getElementById('markDoneBtn');
  if (completedDays[dayNum]) {
    markBtn.textContent = '✅ Выполнено!';
    markBtn.style.opacity = '0.8';
  } else {
    markBtn.textContent = '✅ Выполнено';
    markBtn.style.opacity = '1';
  }

  // Упражнения
  let exercisesHtml = '<ul id="exercises-list">';
  w.exercises.forEach((ex, idx) => {
    const exId = `ex-${dayNum}-${idx}`;
    exercisesHtml += `
      <li>
        ${ex}
        <button class="how-btn" data-id="${exId}">🔍 Как выполнять</button>
        <div class="how-modal" id="${exId}" style="display:none;">
          <div class="how-content">
            <h4>${ex}</h4>
            <p class="how-text">${getExerciseDescription(ex)}</p>
            <button class="close-btn" data-close="${exId}">✕ Закрыть</button>
          </div>
        </div>
      </li>
    `;
  });
  exercisesHtml += '</ul>';

  document.getElementById('workouts').innerHTML = `
    <div class="day-card">
      <h2>${w.title}</h2>
      ${exercisesHtml}
    </div>
  `;

  // Еда
  let mealsHtml = '<div class="meal-plan"><h3>Кето-питание — День ' + m.day + '</h3>';
  m.meals.forEach((meal, idx) => {
    const recipeId = `recipe-${dayNum}-${idx}`;
    mealsHtml += `
      <div class="meal">
        <h4>${meal.type}</h4>
        <p>${meal.desc}</p>
        <button class="recipe-btn" data-id="${recipeId}">📖 Как готовить</button>
        <div class="recipe-modal" id="${recipeId}" style="display:none;">
          <div class="recipe-content">
            <h4>${meal.name}</h4>
            <p class="how-text">${getRecipe(meal.name)}</p>
            <button class="close-btn" data-close="${recipeId}">✕ Закрыть</button>
          </div>
        </div>
      </div>
    `;
  });
  mealsHtml += '</div>';

  document.getElementById('meals').innerHTML = mealsHtml;
  renderProgress();
}

// ========== ИНИЦИАЛИЗАЦИЯ ==========
document.addEventListener('DOMContentLoaded', () => {
  renderDay(currentDay);

  document.getElementById('prevBtn').addEventListener('click', () => {
    if (currentDay > 1) {
      currentDay--;
      localStorage.setItem('currentDay', currentDay);
      renderDay(currentDay);
    }
  });

  document.getElementById('nextBtn').addEventListener('click', () => {
    if (currentDay < 30) {
      currentDay++;
      localStorage.setItem('currentDay', currentDay);
      renderDay(currentDay);
    }
  });

  document.getElementById('markDoneBtn').addEventListener('click', () => {
    completedDays[currentDay] = true;
    localStorage.setItem('completedDays', JSON.stringify(completedDays));
    renderDay(currentDay);
  });

  // Модалки — упражнения
  document.addEventListener('click', (e) => {
    if (e.target.classList.contains('how-btn')) {
      const id = e.target.dataset.id;
      document.getElementById(id).style.display = 'flex';
    }
  });

  // Модалки — рецепты
  document.addEventListener('click', (e) => {
    if (e.target.classList.contains('recipe-btn')) {
      const id = e.target.dataset.id;
      document.getElementById(id).style.display = 'flex';
    }
  });

  // Закрытие модалок
  document.addEventListener('click', (e) => {
    if (e.target.classList.contains('close-btn')) {
      const id = e.target.dataset.close;
      document.getElementById(id).style.display = 'none';
    }
    if (e.target.classList.contains('how-modal') || e.target.classList.contains('recipe-modal')) {
      e.target.style.display = 'none';
    }
  });
});
